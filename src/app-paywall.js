function detectLanguage(message) {
  const lower = message.toLowerCase();
  console.log(`üïµÔ∏è Analyzing message: "${message}"`);
  
  let scores = { pl: 0, de: 0, en: 0 };
  
  // POLISH CHARACTERS (strongest indicator)
  if (lower.match(/[ƒÖƒáƒô≈Ç≈Ñ√≥≈õ≈∫≈º]/)) scores.pl += 5;
  
  // POLISH KEYWORDS (high priority)
  const polishWords = ['cze≈õƒá', 'hej', 'jestem', 'chcƒô', 'chcia≈Çbym', 'trening', 'dieta', 'si≈Çownia', 'ƒáwiczenia', 'zdrowie', 'dziƒôki', 'witaminy', 'przyjmowaƒá', 'jakƒÖ', 'mogƒô', 'hashimoto', 'siƒô'];
  polishWords.forEach(word => {
    if (lower.includes(word)) scores.pl += 3;
  });
  
  // GERMAN KEYWORDS
  const germanWords = ['hallo', 'ich', 'bin', 'm√∂chte', 'training', 'ern√§hrung', 'danke', 'bitte', 'gut', 'wie', 'was', 'fitnessstudio'];
  germanWords.forEach(word => {
    if (lower.includes(word)) scores.de += 2;
  });
  
  // ENGLISH KEYWORDS (lower priority to avoid false positives)
  const englishWords = ['hello', 'want', 'would', 'workout', 'nutrition', 'fitness', 'gym', 'exercise', 'health', 'thanks'];
  englishWords.forEach(word => {
    if (lower.includes(word)) scores.en += 2;
  });
  
  console.log(`üìä Language scores:`, scores);
  
  // Find highest score
  const maxScore = Math.max(...Object.values(scores));
  if (maxScore === 0) return 'pl'; // Default Polish
  
  const detectedLang = Object.keys(scores).find(lang => scores[lang] === maxScore);
  console.log(`üéØ Detected language: ${detectedLang}`);
  
  return detectedLang;
}

